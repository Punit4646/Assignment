public class CharacterAtIndex {
    public static void main(String[] args) {
        String str = "Tops Technologies!";
        int position = 0;
        System.out.println("The character at position " + position + " is " + str.charAt(position));

        position = 10;
        System.out.println("The character at position " + position + " is " + str.charAt(position));
    }
}
